using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;
using _24DH112135_MyStore.Models;

namespace _24DH112135_MyStore.Areas.Admin.Controllers
{
    public class CategoriesController : Controller
    {
        private MyStoreEntities db = new MyStoreEntities();

        // GET: Admin/Categories
        // GET: lấy dữ liệu trong từng bảng Category trong DB để hiển thị
        public ActionResult Index()
        {
            return View(db.Categories.ToList());
        }

        // GET: Admin/Categories/Details/5
        // Details: lấy chi tiết 1 bảng ghi có CategoryID = id
        // int? có thể truyền id rỗng
        public ActionResult Details(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest); // mã lỗi 400: không tìm thấy ID truyền vào
            }
            Category category = db.Categories.Find(id);
            if (category == null)   //không tìm thấy bản ghi
            {
                return HttpNotFound();  //erorr 404
            }
            return View(category);
        }

        // GET: Admin/Categories/Create
        // Load form Create
        public ActionResult Create()
        {
            return View();
        }

        // POST: Admin/Categories/Create
        // POST: lưu dữ liệu nhập từ form Create vào DB
        // To protect from overposting attacks, enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create([Bind(Include = "CategoryID,CategoryName")] Category category)
        {
            if (ModelState.IsValid)
            {
                db.Categories.Add(category);
                db.SaveChanges();
                return RedirectToAction("Index");
            }

            return View(category);
        }

        // GET: Admin/Categories/Edit/5
        // GET: lấy dữ liệu của 1 danh mục đã tồn tại sao cho CategoryID = id
        public ActionResult Edit(int? id)
        {
            return Details(id);
            //if (id == null)
            //{
